def month_name(day_number):
    result = ''
    if day_number == 1:
        result = 'styczen'
    elif day_number == 2:
        result = 'luty'
    elif day_number == 3:
        result = 'marzec'
    elif day_number == 4:
        result = 'kwiecien'
    elif day_number == 5:
        result = 'maj'
    elif day_number == 6:
        result = 'czerwiec'
    elif day_number == 7:
        result = 'lipiec'
    elif day_number == 8:
        result = 'sierpien '
    elif day_number == 9:
        result = 'wrzesien'
    elif day_number == 10:
        result = 'pazdiernik'
    elif day_number == 11:
        result = 'listopad'
    elif day_number == 12:
        result = 'grudzien'
    
    return result


